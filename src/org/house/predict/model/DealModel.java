package org.house.predict.model;
import java.util.*;
public class DealModel {
private int transId;
private int price;
private int pid; 
public int getTransId() {
	return transId;
}
public void setTransId(int transId) {
	this.transId = transId;
}
public int getPrice() {
	return price;
}
public void setPrice(int price) {
	this.price = price;
}
public int getPid() {
	return pid;
}
public void setPid(int pid) {
	this.pid = pid;
}
public Date getDate() {
	return date;
}
public void setDate(Date date) {
	this.date = date;
}
private Date date;
}
